
public class Olimpiadas {
	private int ano;
	private String Tipo;

	public Olimpiadas(int ano, String tipo) {
		super();
		this.ano = ano;
		Tipo = tipo;
	}

	public int getAno() {
		return ano;
	}

	public void setAno(int ano) {
		this.ano = ano;
	}

	public String getTipo() {
		return Tipo;
	}

	public void setTipo(String tipo) {
		Tipo = tipo;
	}

}
